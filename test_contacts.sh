#!/bin/bash

# Тестирование API контактов с использованием curl
# База данных: api/my_database.db
# Сервер должен быть запущен через uvicorn main:app --reload --host 0.0.0.0 --port 8000 -w "%(server_time).3f"

BASE_URL="http://localhost:8000"

echo "=========================================="
echo "Тестирование API контактов (FastAPI)"
echo "=========================================="

# ==========================================
# ТЕСТ 1: Регистрация пользователя (POST /auth)
# ==========================================
echo ""
echo "[ТЕСТ 1] Регистрация нового пользователя..."

RESPONSE=$(curl -s -w "\nHTTP_CODE:%{http_code}" \
    -X POST "$BASE_URL/auth" \
    -H "Content-Type: application/json" \
    -d '{"username":"user","password":"pass"}')
echo "Ответ сервера:"
echo "$RESPONSE"

# Извлекаем токен из ответа (если успешная регистрация)
TOKEN=$(echo "$RESPONSE" | jq -r '.token // empty')

if [ ! "$TOKEN" ]; then
    echo "Ошибка: Не удалось получить токен. Пропускаем тесты контактов."
    exit
else
    echo ""
    echo "[INFO] Токен получен успешно!"
fi

# ==========================================
# ТЕСТ 2: Получение списка контактов (GET /contacts)
# ==========================================
echo ""
echo "[ТЕСТ 2] Получение списка всех контактов..."

RESPONSE=$(curl -s -w "\nHTTP_CODE:%{http_code}" \
    "$BASE_URL/contacts" \
    -H "X-Token: $TOKEN")

echo "Ответ сервера:"
echo "$RESPONSE" | tail -n 1

# ==========================================
# ТЕСТ 3: Создание нового контакта (POST /contact)
# ==========================================
echo ""
echo "[ТЕСТ 3] Создание нового контакта..."

CONTACT_ID=$(curl -s \
    -X POST "$BASE_URL/contact" \
    -H "Content-Type: application/json" \
    -H "X-Token: $TOKEN" \
    -d '{"name":"Иван Иванов","phone_number":"+7 (900) 123-45-67","date_of_birth":"1990-05-15","notes":"Новый контакт"}' | jq '.id')

echo "Ответ сервера:"
curl -s \
    -X POST "$BASE_URL/contact" \
    -H "Content-Type: application/json" \
    -H "X-Token: $TOKEN" \
    -d '{"name":"Иван Иванов","phone_number":"+7 (900) 123-45-67","date_of_birth":"1990-05-15","notes":"Новый контакт"}'

# ==========================================
# ТЕСТ 4: Получение конкретного контакта (GET /contact/{id})
# ==========================================
echo ""
echo "[ТЕСТ 4] Получение конкретного контакта по ID..."

curl -s \
    "$BASE_URL/contact/$CONTACT_ID" \
    -H "X-Token: $TOKEN" | jq .

# ==========================================
# ТЕСТ 5: Обновление контакта (PUT /contact)
# ==========================================
echo ""
echo "[ТЕСТ 5] Обновление существующего контакта..."

curl -s \
    -X PUT "$BASE_URL/contact" \
    -H "Content-Type: application/json" \
    -H "X-Token: $TOKEN" \
    -d '{"id":'$CONTACT_ID',"name":"Иван Иванов (обновленный)","phone_number":"+7 (900) 123-45-68","date_of_birth":"1990-05-16","notes":"Обновленные примечания"}' | jq .

# ==========================================
# ТЕСТ 6: Удаление контакта (DELETE /contact/{id})
# ==========================================
echo ""
echo "[ТЕСТ 6] Удаление контакта..."

curl -s \
    -X DELETE "$BASE_URL/contact/$CONTACT_ID" \
    -H "X-Token: $TOKEN" | jq . \

# ==========================================
# ТЕСТ 7: Получение списка контактов после удаления (GET /contacts)
# ==========================================
echo ""
echo "[ТЕСТ 7] Проверка списка контактов после удаления..."

curl -s \
    "$BASE_URL/contacts" \
    -H "X-Token: $TOKEN" | jq .

# ==========================================
# ТЕСТ 8: Получение несуществующего контакта (GET /contact/{id})
# ==========================================
echo ""
echo "[ТЕСТ 8] Попытка получить несуществующий контакт..."

curl -s \
    "$BASE_URL/contact/999" \
    -H "X-Token: $TOKEN" | jq .

# ==========================================
# ТЕСТ 9: Создание контакта с невалидной датой (POST /contact)
# ==========================================
echo ""
echo "[ТЕСТ 9] Создание контакта с валидными данными..."

curl -s \
    -X POST "$BASE_URL/contact" \
    -H "Content-Type: application/json" \
    -H "X-Token: $TOKEN" \
    -d '{"name":"Петр Петров","phone_number":"+7 (900) 234-56-78","date_of_birth":"1985-08-22"}' | jq .

echo ""
echo "=========================================="
echo "Тестирование завершено!"
echo "=========================================="
